package exc;

public class ReceptionistNotFoundException extends Exception {

	public ReceptionistNotFoundException() {
	}

	public ReceptionistNotFoundException(String arg0) {
		super(arg0);
	}

	public ReceptionistNotFoundException(Throwable arg0) {
		super(arg0);
	}

	public ReceptionistNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ReceptionistNotFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
